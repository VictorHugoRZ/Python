
# Uso de operadores de Asignacion y Reasignacion

# Operador de Asignacion
miVariable = 10
print(miVariable)  # 10

# Reasignamos el valor de la variable usando el valor anterior
miVariable = miVariable + 1
print(miVariable)  # 11

# Reasignasion de valor
miVariable += 1
print(miVariable)  # 12

miVariable -= 2
print(miVariable)  # 10

miVariable *= 3
print(miVariable)  # 30

miVariable /= 2
print(miVariable)  # 15

miVariable //= 2
print(miVariable)  # 7

miVariable **= 2
print(miVariable)  # 49
